    public VisitasProgramadas(Visita v) {
        initComponents();
        
        this.v = v;
        c = cdao.getClienteId(v.getFkIdCliente());
        f = fdao.getFuncionarioId(v.getFkIdFuncionario());
        
        cmbCliente.addItem(c.getCpf());
        fdao.getFuncionariosLogin(cmbFuncionario);
        cmbFuncionario.setSelectedItem(f.getLogin());
        try {
            dchDataPrevista.setDate(sdf.parse(v.getData()));
        } catch (ParseException ex) {
            Logger.getLogger(VisitasProgramadas.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtObservacao.setText(v.getDetalhes());
        
        if(this.v.getConcluido().equals("S")) {
            rbnSim.setSelected(true);
        } else {
            rbnNao.setSelected(true);
        }
        HistoricoVisitas.visitasDialog.setTitle("Editar visita");
    }
    
