    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(verificarCampos()) {
            VisitaDAO vdao = new VisitaDAO();
            c.setCpf(cmbCliente.getSelectedItem().toString());
            v.setData(sdf.format(dchDataPrevista.getDate()));
            v.setDetalhes(txtObservacao.getText());
            if(v.getId() <= 0) {
                c = cdao.getClienteCPF(c);
                v.setFkIdCliente(c.getId());
                f = fdao.getFuncionarioLogin(cmbFuncionario.getSelectedItem().toString());
                v.setFkIdFuncionario(f.getId());
            }
            vdao.salvar(v);
            limparCampos();
            HistoricoVisitas.visitasDialog.dispose();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Todos os campos são obrigatórios!!");
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

