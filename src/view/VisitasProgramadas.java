    public void limparCampos() {
        dchDataPrevista.cleanup();
        buttonGroup1.clearSelection();
        txtObservacao.setText(null);
    }
    
    public boolean verificarCampos() {
        boolean podeSalvar = true;

        try {
            
            if (buttonGroup1.getSelection().isSelected()) {
                podeSalvar = true;
            }
            if (cmbCliente.getSelectedItem().toString().isEmpty()) {
                podeSalvar = false;
            }
            if (cmbFuncionario.getSelectedItem().toString().isEmpty()) {
                podeSalvar = false;
            }
            if (dchDataPrevista.getDate().toString().isEmpty()) {
                podeSalvar = false;
            }

        } catch(NullPointerException e) {
            podeSalvar = false;
        }
        return podeSalvar;
    }
    
